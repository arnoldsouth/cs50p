# it will display that the class of 50 is int
print(type(50))
# <class 'int'>


# it will display that the class str
print(type("hello, world"))
# <class 'str'>


# it will display that the class list
print(type([]))
# <class 'list'>


# using the name of Python’s built-in list class
# it will display that the class list
print(type(list()))
# <class 'list'>


# it will display that the class dict
print(type({}))
# <class 'dict'>


# using the name of Python’s built in dict class
# it will display that the class dict
print(type(dict()))
# <class 'dict'>
